func parseScopes(csv string) []auth.Scope {
	parts := strings.Split(csv, ",")
	out := make([]auth.Scope, 0, len(parts))
	for _, p := range parts {
		p = strings.TrimSpace(p)
		if p == "" {
			continue
		}
		out = append(out, auth.Scope(p))
	}
	return out
}
